package dti.oasis.recordset;

/**
 * This class provides a default implementation of the RecordLoadProcessor interface that does nothing.
 * It is a convenience class so that extending classes only need to implement the required methods.
 * <p/>
 * <p>(C) 2003 Delphi Technology, inc. (dti)</p>
 * Date:   Oct 18, 2006
 *
 * @author wreeder
 */
/*
 *
 * Revision Date    Revised By  Description
 * ---------------------------------------------------
 *
 * ---------------------------------------------------
 */
public class DefaultRecordLoadProcessor implements RecordLoadProcessor {
    public static RecordLoadProcessor DEFAULT_INSTANCE = new DefaultRecordLoadProcessor();

    /**
     * Process the given record after it's been loaded.
     *
     * @param record             the current record
     * @param rowIsOnCurrentPage true if this record is on the current page
     */
    public boolean postProcessRecord(Record record, boolean rowIsOnCurrentPage) {
        // Do Nothing
        return true;
    }

    /**
     * Process the RecordSet after all records have been loaded and processed..
     *
     * @param recordSet the record set.
     */
    public void postProcessRecordSet(RecordSet recordSet) {
        // Do Nothing
    }

}
